public class PruebaSwitch1{
	public static void main(String[] args){
		int i = 3;

		switch(i){
			case 0:
				System.out.println("Cero");
				break;
			case 1:
				System.out.println("Uno");
				break;
			default:
				System.out.println("No se si esto será impreso algún día");
				break;
			case 2:
				System.out.println("Dos");
				break;
		}
	}
} 